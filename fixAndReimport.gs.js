// ヘルパー: ローカル時間の日時を作成
function dt(y, m, day, h, min) {
  return new Date(y, m - 1, day, h, min, 0);
}

// ヘルパー: ローカル時間の日付のみを作成
function d(y, m, day) {
  return new Date(y, m - 1, day);
}
